export class UserTable {
    user_id:any;
	user_name:any;
	user_email:any;
	user_password:any;


    constructor(user_id: any, user_name: any, user_email: any, user_password: any) {
        this.user_id = user_id;
        this.user_name = user_name;
        this.user_email = user_email;
        this.user_password = user_password;
      
    }
}
